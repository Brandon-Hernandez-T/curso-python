tema_curso = "== Operadores Aritmeticos =="
print(tema_curso)

#Suma o adiccion
numeroUno = 1
numeroDos = 2
suma = numeroUno + numeroDos
print("Resultado de sumar " + str(numeroUno) + " + " + str(numeroDos) + " es: " + str(suma) )

#Resta o substraccion
numeroTres = 3
numeroCuatro = 4
resta = numeroCuatro - numeroTres
print("Resultado de restar " + str(numeroCuatro) + " - " + str(numeroTres) + " es: " + str(resta) )

#Multiplicacion
numeroCinco = 5
numeroSeis = 6
multiplicacion = numeroCinco * numeroSeis
print("Resultado de multiplicar " + str(numeroCinco) + " * " + str(numeroSeis) + " es: " + str(multiplicacion) )

#Exponente o potencia
expontente = numeroCinco ** numeroDos
print("Resultado de exponente de " + str(numeroCinco) + " ** " + str(numeroDos) + " es: " + str(expontente) )

#Division
division = numeroCinco / numeroDos
print("Resultado de dividir " + str(numeroCinco) + " / " + str(numeroDos) + " es: " + str(division) )

#Division entera (Solo regresa el valor entero, sin putno decimal)
divisionEntera = numeroCinco // numeroTres
print("Resultado de dividir " + str(numeroCinco) + " // " + str(numeroTres) + " es: " + str(divisionEntera) )


#Modulo o resto (resultado despues de dividir)
resto = numeroCinco % numeroDos
print("Resultado de resto " + str(numeroCinco) + " % " + str(numeroDos) + " es: " + str(resto) )
